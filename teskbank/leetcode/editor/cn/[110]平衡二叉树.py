# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def isBalanced(self, root: TreeNode) -> bool:
        self.isbalance = True
        def helper(root, level):
            if not root:
                return level
            left = helper(root.left, level + 1)
            right = helper(root.right, level + 1)
            if not self.isbalance: # 早停
                return
            if abs(right - left) > 1:
                self.isbalance = False
            return max(left, right)
        helper(root, 0)
        return  self.isbalance
